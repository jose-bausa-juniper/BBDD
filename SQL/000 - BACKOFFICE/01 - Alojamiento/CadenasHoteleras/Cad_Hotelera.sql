USE BD_Nincoming

-- TABLAS CON ID_CAD
SELECT * FROM INFORMATION_SCHEMA.COLUMNS WHERE COLUMN_NAME LIKE '%Id_Cad%'

--CADENAS SIN NOMBRE
SELECT
	CH.Id_Cad,
	INCH.Ica_Nombre
FROM
				Tbl_CadenaHotelera	CH
	LEFT JOIN	Tbl_IDiNCad			INCH ON INCH.Id_Cad = CH.Id_Cad
WHERE
	1 = 1
	AND	INCH.Ica_Nombre IS NULL
GROUP BY
	CH.Id_Cad,
	INCH.Ica_Nombre

